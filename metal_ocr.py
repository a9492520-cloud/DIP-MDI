def _unpack_template(data):
    t = np.zeros((T_H, T_W), dtype=np.uint8)
    for j in range(T_H):
        row_val = (data[j * 2] << 8) | data[j * 2 + 1]
        for i in range(T_W):
            t[j, i] = 255 if ((row_val >> (15 - i)) & 1) else 0
    return t

def _build_templates():
    digits = [_unpack_template(d) for d in dT_data]
    letters = [_unpack_template(l) for l in lT_data]
    chars = [chr(ord('0') + i) for i in range(10)] + [chr(ord('A') + i) for i in range(26)]
    templates = digits + letters
    return templates, chars

TEMPLATES, TEMPLATE_CHARS = _build_templates()
TEMPLATES_NP = np.array(TEMPLATES, dtype=np.float64).reshape(36, -1)

def _imread_unicode(path):
    try:
        with open(path, 'rb') as f:
            data = f.read()
        img = cv2.imdecode(np.frombuffer(data, np.uint8), cv2.IMREAD_COLOR)
        return img
    except:
        return None

def get_lot_roi(img):
    h, w = img.shape[:2]
    roi_x = int(w * 0.20)
    roi_y = int(h * 0.40)
    roi_w = int(w * 0.65)
    roi_h = int(h * 0.40)
    if roi_x + roi_w > w: roi_w = w - roi_x
    if roi_y + roi_h > h: roi_h = h - roi_y
    if roi_w < 1: roi_w = 1
    if roi_h < 1: roi_h = 1
    roi = img[roi_y:roi_y + roi_h, roi_x:roi_x + roi_w]
    return roi, roi_x, roi_y, roi_w, roi_h

def _gaussian_blur_5x5(f):
    kernel = np.array([
        [1,4,6,4,1],
        [4,16,24,16,4],
        [6,24,36,24,6],
        [4,16,24,16,4],
        [1,4,6,4,1]
    ], dtype=np.float32) / 256.0
    return cv2.filter2D(f, -1, kernel, borderType=cv2.BORDER_REPLICATE)

def _enhance_contrast(gray):
    blurred = cv2.boxFilter(gray, -1, (31, 31))
    enhanced = gray.astype(np.float32) + 0.4 * (gray.astype(np.float32) - blurred.astype(np.float32))
    enhanced = np.clip(enhanced, 0, 255).astype(np.uint8)
    return enhanced

def _adaptive_threshold(f, blockSize=21, C=3):
    half = blockSize // 2
    sigma = blockSize / 6.0
    yy, xx = np.mgrid[-half:half+1, -half:half+1]
    weights = np.exp(-(xx*xx + yy*yy) / (2.0 * sigma * sigma))
    weights_sum = cv2.filter2D(np.ones_like(f, dtype=np.float32), -1, weights, borderType=cv2.BORDER_REPLICATE)
    mean = cv2.filter2D(f.astype(np.float32), -1, weights, borderType=cv2.BORDER_REPLICATE) / weights_sum
    result = np.where(f < mean - C, 255, 0).astype(np.uint8)
    return result

def _close_binary(bin_img, radius=4):
    ksize = 2 * radius + 1
    kernel = cv2.getStructuringElement(cv2.MORPH_ELLIPSE, (ksize, ksize))
    return cv2.morphologyEx(bin_img, cv2.MORPH_CLOSE, kernel)

def _find_text_band(binary):
    hproj = np.sum(binary > 0, axis=1)
    hth = max(5, binary.shape[1] // 80)
    ys = np.where(hproj > hth)[0]
    if len(ys) < 5:
        return 0, binary.shape[0] - 1
    breaks = np.where(np.diff(ys) > 5)[0]
    runs = np.split(ys, breaks + 1)
    best_run = max(runs, key=len) if runs else ys
    return int(best_run[0]), int(best_run[-1])

def _segment_characters(bin_img, max_regions=8):
    h, w = bin_img.shape
    vproj = np.sum(bin_img > 0, axis=0, dtype=np.int32)
    mproj = np.copy(vproj)
    mproj[1:-1] = np.maximum(vproj[:-2], np.maximum(vproj[1:-1], vproj[2:]))
    mproj[0] = max(vproj[0], vproj[1])
    mproj[-1] = max(vproj[-1], vproj[-2])
    charThresh = max(5, h // 10) if h > 50 else 3
    gapThresh = max(2, h // 30) if h > 50 else 1
    regions = []
    in_char = False
    start_x = 0
    for i in range(w):
        if mproj[i] > charThresh and not in_char:
            in_char = True
            start_x = i
        elif (mproj[i] <= gapThresh or i == w - 1) and in_char:
            in_char = False
            end_x = i - 1 if mproj[i] <= gapThresh else i
            seg_w = end_x - start_x + 1
            if seg_w >= 3 and len(regions) < max_regions:
                col_slice = bin_img[:, start_x:end_x + 1]
                rows_with_dots = np.any(col_slice > 0, axis=1)
                if np.any(rows_with_dots):
                    y_indices = np.where(rows_with_dots)[0]
                    min_y, max_y = int(y_indices[0]), int(y_indices[-1])
                    seg_h = max_y - min_y + 1
                    if seg_h < 5: seg_h = 5
                    if seg_w >= 4 and seg_h >= 6 and seg_w * seg_h >= 60:
                        regions.append((start_x, min_y, seg_w, seg_h))
    return regions

def _ncc(a, b):
    a = a.ravel().astype(np.float64)
    b = b.ravel().astype(np.float64)
    ma, mb = np.mean(a), np.mean(b)
    da = a - ma
    db = b - mb
    aa = np.sum(da * da)
    bb = np.sum(db * db)
    ab = np.sum(da * db)
    if aa < 1e-10 or bb < 1e-10:
        return 0.0
    return ab / np.sqrt(aa * bb)

def _max_pool_norm(char_img, tgt_w=T_W, tgt_h=T_H):
    h, w = char_img.shape
    out = np.zeros((tgt_h, tgt_w), dtype=np.uint8)
    for j in range(tgt_h):
        y0 = (j * h) // tgt_h
        y1 = ((j + 1) * h) // tgt_h
        if y1 <= y0: y1 = y0 + 1
        for i in range(tgt_w):
            x0 = (i * w) // tgt_w
            x1 = ((i + 1) * w) // tgt_w
            if x1 <= x0: x1 = x0 + 1
            out[j, i] = np.max(char_img[y0:y1, x0:x1])
    return out

def match_char(char_img):
    norm = _max_pool_norm(char_img)
    norm_bin = (norm > 128).astype(np.float64).ravel() * 255.0
    best_score = -1.0
    best_char = '?'
    for idx in range(36):
        s = _ncc(norm_bin, TEMPLATES_NP[idx])
        if s > best_score:
            best_score = s
            best_char = TEMPLATE_CHARS[idx]
    return best_char, best_score

def format_lot_number(raw_chars):
    digit_map = {'O': '0', 'Q': '0', 'D': '0', 'I': '1', 'L': '1', 'Z': '2', 'S': '5', 'G': '6', 'B': '8'}
    letter_map = {'8': 'B', '0': 'O', '1': 'I', '5': 'S', '2': 'Z', '6': 'G'}
    filtered = []
    for c in raw_chars:
        if c and c.isalnum():
            filtered.append(c.upper())
    result = []
    for i in range(6):
        if i < len(filtered):
            c = filtered[i]
        else:
            c = '0' if i < 5 else 'A'
        if i < 5:
            c = digit_map.get(c, c)
            if not c.isdigit(): c = '0'
        else:
            c = letter_map.get(c, c)
            if not c.isalpha(): c = 'A'
        result.append(c)
    return ''.join(result[:6])

def _longest_consecutive_run(arr, max_gap=5):
    if len(arr) == 0:
        return np.array([])
    breaks = np.where(np.diff(arr) > max_gap)[0]
    runs = np.split(arr, breaks + 1)
    return max(runs, key=len)

def recognize(image_path):
    img = _imread_unicode(image_path)
    if img is None:
        return "ERROR"

    gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)
    roi, rx, ry, rw, rh = get_lot_roi(gray)

    roi_big = cv2.resize(roi, None, fx=3, fy=3, interpolation=cv2.INTER_LINEAR)
    enhanced = _enhance_contrast(roi_big)
    smoothed = _gaussian_blur_5x5(enhanced)

    binary = _adaptive_threshold(smoothed, 21, 3)

    y_start, y_end = _find_text_band(binary)
    band = binary[y_start:y_end + 1, :]

    band = _close_binary(band, radius=4)

    regions = _segment_characters(band)

    raw_chars = []
    for (px, py, bw, bh) in regions:
        if len(raw_chars) >= 6:
            break
        char_img = band[py:py + bh, px:px + bw]
        if char_img.size == 0:
            continue
        best_char, best_score = match_char(char_img)
        if best_score > 0.15:
            raw_chars.append(best_char)

    lot_number = format_lot_number(raw_chars)
    return lot_number

